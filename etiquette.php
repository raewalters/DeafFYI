<h3 id="interpreter">What is an interpreter for?</h3>
<p>To facilitate communication between a Deaf and hearing person. There are also Certified Deaf Interpreters (CDI) 
who specialize in cultural and linguistic exchanges.</p>

<h3 id="talk">How do I talk to a Deaf person when there is an interpreter?</h3>
<p>When using an interpreter, their role is to facilitate communication between the Deaf person and the non signer. 
They will speak in first person as the Deaf person, you should look at the Deaf person and speak directly to the them 
instead of “tell him or her”.

<h3 id="attention">How do I get a Deaf person’s attention?</h3>
<p>Waving your arm, tapping on the shoulder, using a third person to get their attention for you, flashing lights, 
tapping the table (to create a vibration) and tapping your foot on the floor (again, for vibrations).</p>

<h3 id="watch">Is it rude to watch Deaf people signing?</h3>
<p>Yes. It is akin to eavesdropping, except you are staring directly at the people having the conversation, who are 
most likely VERY aware that you are staring at them. It is ok to say hi, and tell them you are learning sign language.</p>

<h3 id="walk">If Deaf people are signing and I need to walk in between them, what do I do?</h3> 
<p>You don’t need to limbo in between them, simply sign “excuse me” and walk right on through.</p>

<h3 id="communicate">How do I know how to communicate with a Deaf person?</h3>
<p>The deaf person will let you know their prefered method of communication, whether it is speaking, reading lips, 
gesturing, signing, writing, etc. No need to worry if you don't know ASL. A pen a paper (or even a cell phone to type on)
is acceptable. Whatever you do, Do NOT shout! Shouting does not help them understand you. </p>

