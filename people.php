<h3 id="community">What is the Deaf Community?</h3>
<p>Each Deaf community is a cultural group which shares a sign language and a common heritage. Members of Deaf 
communities all around the world therefore identify themselves as members of a cultural and linguistic group. 
Identification with the Deaf community is a personal choice and is usually made independent of the individual’s 
hearing status, and the community is not automatically composed of all people who are Deaf or hard of hearing.  
The Deaf community may also include family members of Deaf people, sign language interpreters and people who work or 
socialize with Deaf people who identify with Deaf culture. A person is a member of the Deaf community if he or she 
self-identifies as a member of the Deaf community, and if other members accept that person as a member. Very often 
this acceptance is strongly linked to competence in a signed language.</p>

<h3 id="culture">What is Deaf Culture?</h3>
<p> Deaf people as a linguistic minority have a common experience of life, and this manifests itself in Deaf culture. 
This includes beliefs, attitudes, history, norms, values, literary traditions, and art shared by Deaf people.
When someone is culturally Deaf if means that they use American Sign Lamnguage as their primary language and consider 
themselves a part of the Deaf Community.</p>

<h3 id="bigdeaf">What is the difference between deaf and Deaf?</h3>
<p>Lowercase deaf is used when referring to the audiological condition of not hearing, and the uppercase Deaf when 
referring to a particular group of deaf people who share a language – American Sign Language (ASL) – and a culture.  
The members of this group have inherited their sign language, use it as a primary means of communication among themselves, 
and hold a set of beliefs about themselves and their connection to the larger society.  We distinguish them from, 
for example, those who find themselves losing their hearing because of illness, trauma or age; although these people 
share the condition of not hearing, they do not have access to the knowledge, beliefs, and practices that make up the c
ulture of Deaf people.</p>

<h3 id="readlips">Can all Deaf people read lips?</h3>
<p>No, not all hearing people can either! Reading lips is not something specifically that Deaf people can do, it is 
something that Deaf people HAVE done and sometimes need to do in order to exist and participate in a conversation where 
signing is not an option. Most people can not read lips, it is also virtually impossible to read lips accurately 100%.</p>

<h3 id="talk">Can all Deaf people talk?</h3>
<p>No, it is a personal preference whether the Deaf person prefers to use their voice or not. There is much controversy 
surrounding this and history books to explain why. The short answer is that not all Deaf people took speech class, want 
to speak, feel comfortable speaking or have ever used their voice. It is afterall, their choice.</p>

<h3 id="aids">Why don’t all Deaf people wear hearing aids?</h3>
<p>Many Deaf persons use hearing aids and they work great for them, for others sound amplification does not work. It 
depends on where your hearing is in decibels. It is the individual’s choice, based on their body and preferences.</p> 

<h3 id="drive">Can Deaf people drive?</h3>
<p>Of course.</p>

<h3 id="braille">Can Deaf people read braille?</h3>
<p>Sure, if they’ve learned how to, HOWEVER, they are Deaf, not blind.</p>

<h3 id="door">How do Deaf people know when someone is at the door?</h3>
<p>Glass doors help, but in general we have doorbells that are attached to the lights, every time sometime rings the 
doorbell the lights flash.</p>

<h3 id="phone">How do Deaf people talk on the phone?</h3>
<p>TTYs, videophones, Facetime, video texting, text messaging, Skype, etc.</p>


