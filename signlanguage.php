<h3 id="universal">Is sign language universal?</h3>
<p>Sign Language is NOT universal, different countries have their own sign languages. No one form of sign language is 
universal. For example, British Sign Language (BSL) is a different language from ASL, and Americans who know ASL may not 
understand BSL.</p>

<h3 id="ASL"> What is American Sign Language?</h3>
<p>American Sign Language (ASL) is a visual language. With signing, the brain processes linguistic information through the 
eyes. The shape, placement, and movement of the hands, as well as facial expressions and body movements, all play important 
parts in conveying information. ASL is used predominantly in the United States and in many parts of Canada. ASL is accepted 
by many high schools, colleges, and universities in fulfillment of modern and “foreign” language academic degree 
requirements across the United States.</p>

<h3 id="grammar">Does ASL have grammar?</h3>
<p>Yes. ASL is NOT simply signing as you would speak English. ASL has its own grammar, syntax and sentence structure that is 
different from English.</p>

<h3 id="gesturing">Is ASL a form of gesturing?</h3>
<p>No, Deaf people will gesture if they are trying to emphasize something or communicate without sign, gesturing and ASL 
are NOT the same. Gesturing is not a language with rules of grammar and structure. ASL is a langage while gestures is a 
game played at games around the holidays.</p>

<h3 id="compare">How does ASL compare with spoken language?</h3>
<p>In spoken language, words are produced by using the mouth and voice to make sounds. But for people who are deaf 
(particularly those who are profoundly deaf), the sounds of speech are often not heard, and only a fraction of speech 
sounds can be seen on the lips. Sign languages are based on the idea that vision is the most useful tool a deaf person 
has to communicate and receive information.</p>
<p>ASL is a language completely separate and distinct from English. It contains all the fundamental features of 
language—it has its own rules for pronunciation, word order, and complex grammar. While every language has ways of 
signaling different functions, such as asking a question rather than making a statement, languages differ in how this 
is done. For example, English speakers ask a question by raising the pitch of their voice; ASL users ask a question by 
raising their eyebrows, widening their eyes, and tilting their bodies forward.</p>
<p>Just as with other languages, specific ways of expressing ideas in ASL vary as much as ASL users do. In addition to 
individual differences in expression, ASL has regional accents and dialects. Just as certain English words are spoken 
differently in different parts of the country, ASL has regional variations in the rhythm of signing, form, and pronunciation.
Ethnicity and age are a few more factors that affect ASL usage and contribute to its variety.</p>

